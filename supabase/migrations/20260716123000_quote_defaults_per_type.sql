  $$הסטודיו אינו אחראי לסטיות צבע במסכים ובהדפסה.$$,
  $$הפרויקט מתבצע במסגרת וורדפרס + אלמנטור. מערכות נוספות שיידרשו מעבר למוסכם יתומחרו בנפרד. הפלטפורמה המדויקת תיקבע לאחר שיחת האפיון.$$,

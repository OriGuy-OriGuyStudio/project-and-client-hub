-- ============================================================
-- Quote v2.1-C , per-product quote defaults + full legal seed.
-- quote_defaults goes from one global row to one row per type
-- (website/system/automation). Each type seeds its own legal clause set:
-- website = Ori's 14 approved clauses + 5 additions; system/automation = the
-- per-product variants (site-specific clauses dropped/adapted). See spec
-- 2026-07-16 §3 / §8. Applied to the branch DB (dbchappsqcsixxecxzqv) only.
-- ============================================================

alter table public.quote_defaults
  add column if not exists type text not null default 'website'
  check (type in ('website', 'system', 'automation'));

alter table public.quote_defaults
  add constraint quote_defaults_type_key unique (type);

-- Website: full 19-clause set (Ori's 14 verbatim + additions 15-19).
update public.quote_defaults set legal = jsonb_build_array(
  $$הסטודיו יבצע את העבודה על הצד הטוב ביותר לפי שיקול דעת מקצועי, ויעשה מיטב יכולתו לעמוד בציפיות הלקוח.$$,
